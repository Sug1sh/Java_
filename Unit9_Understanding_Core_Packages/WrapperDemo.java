
/*Qn)Write a program to illustrate Integer and Double class as a wrapper class of int and double
respectively.*/

package Unit9_Understanding_Core_Packages;

class WrapperDemo {
    public static void main(String[] args) {
        Integer a = 10;
        System.out.println("Integer Value after converting the primitive to the wrapper class : " + a);
        
        Double b = 10.0;
        System.out.println("Double Value after converting the primitive to the wrapper class: " + b);
    }
}

